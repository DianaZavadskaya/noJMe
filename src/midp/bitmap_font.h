/* Decode a UTF-8 character and return the Unicode codepoint.
 * Returns the codepoint and advances *index past the character.
 * Returns -1 for invalid UTF-8. */
static inline int utf8_decode(const char* str, int* index, int len) {
    if (*index >= len) return -1;
    
    unsigned char c = (unsigned char)str[*index];
    (*index)++;
    
    /* ASCII character (0x00-0x7F) */
    if (c < 0x80) {
        return c;
    }
    
    /* Multi-byte character */
    int codepoint;
    int bytes_left;
    
    if ((c & 0xE0) == 0xC0) {
        /* 2-byte character: 110xxxxx 10xxxxxx */
        codepoint = c & 0x1F;
        bytes_left = 1;
    } else if ((c & 0xF0) == 0xE0) {
        /* 3-byte character: 1110xxxx 10xxxxxx 10xxxxxx */
        codepoint = c & 0x0F;
        bytes_left = 2;
    } else if ((c & 0xF8) == 0xF0) {
        /* 4-byte character: 11110xxx 10xxxxxx 10xxxxxx 10xxxxxx */
        codepoint = c & 0x07;
        bytes_left = 3;
    } else {
        /* Invalid UTF-8 start byte */
        return -1;
    }
    
    /* Read continuation bytes */
    for (int i = 0; i < bytes_left; i++) {
        if (*index >= len) return -1;
        c = (unsigned char)str[*index];
        if ((c & 0xC0) != 0x80) return -1;  /* Not a continuation byte */
        (*index)++;
        codepoint = (codepoint << 6) | (c & 0x3F);
    }
    
    return codepoint;
}

/* Get font data for a Unicode codepoint.
 * Returns pointer to 7 bytes of bitmap data, or space glyph if not found. */
static inline const uint8_t* get_char_data_unicode(int codepoint) {
    /* Range 1: Basic ASCII (0x20-0x7E) */
    if (codepoint >= FONT_RANGE1_START && codepoint <= FONT_RANGE1_END) {
        return &bitmap_font[(codepoint - FONT_RANGE1_START) * FONT_HEIGHT];
    }
    
    /* Range 2: Latin-1 Supplement (0xA0-0xFF) */
    if (codepoint >= FONT_RANGE2_START && codepoint <= FONT_RANGE2_END) {
        int index = FONT_RANGE1_COUNT + (codepoint - FONT_RANGE2_START);
        return &bitmap_font[index * FONT_HEIGHT];
    }
    
    /* Range 3: Cyrillic (0x400-0x45F) */
    if (codepoint >= FONT_RANGE3_START && codepoint <= FONT_RANGE3_END) {
        int index = FONT_RANGE1_COUNT + FONT_RANGE2_COUNT + (codepoint - FONT_RANGE3_START);
        return &bitmap_font[index * FONT_HEIGHT];
    }
    
    /* Character not in font - return space */
    return &bitmap_font[0];
}

/* Legacy function for backward compatibility - handles single byte as Latin-1 */
static inline const uint8_t* get_char_data(char c) {
    /* For ASCII range, use the codepoint directly */
    if ((unsigned char)c >= 0x20 && (unsigned char)c <= 0x7E) {
        return get_char_data_unicode((unsigned char)c);
    }
    /* For extended Latin-1 characters (negative char values in signed char) */
    if ((unsigned char)c >= 0xA0 && (unsigned char)c <= 0xFF) {
        return get_char_data_unicode((unsigned char)c);
    }
    /* Return space for unknown characters */
    return &bitmap_font[0];
}

/* Count the number of characters (not bytes) in a UTF-8 string */
static inline int utf8_strlen(const char* str) {
    if (!str) return 0;
    
    int len = 0;
    int i = 0;
    int byte_len = 0;
    while (str[byte_len]) byte_len++;
    
    while (i < byte_len) {
        int cp = utf8_decode(str, &i, byte_len);
        if (cp >= 0) len++;
    }
    
    return len;
}

